/**
 * This abstract class encapsulates a task in the simulation.
 *
 * @author Keagan Pang
 *
 */
public abstract class Task {

  /**
   * Constructor for a task.
   *
   */
  public Task() {
  }

  /**
   * Abstract method to convert a task to its String representation.
   */
  @Override
  public abstract String toString();

}
